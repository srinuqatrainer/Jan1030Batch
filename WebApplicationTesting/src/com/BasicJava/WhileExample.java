package com.BasicJava;

public class WhileExample {

	public static void main(String[] args)
	{
	
		int k=1;
		while(k<=5)
		{
			System.out.println("Selenium");
			k++;
			
		}
		
		System.out.println("***********************");
		
		int i=9;
		while(i>=5)
		{
			System.out.println("LiveTech");
			i--;
		}

		
		int p=5;
		while(p>=1)
		{
			System.out.println(p);
			p--;
		}
	}

}
