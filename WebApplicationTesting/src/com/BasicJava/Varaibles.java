package com.BasicJava;

public class Varaibles {

	public static void main(String[] args)
	{
		// a variable can save only one value at a time
		// a variable once declared with a data type need not be declared again
		
		int a=10;
		a=20;
		System.out.println(a);
		
		String b="LiveTech";
		System.out.println(b);
		
		double c=10.123;
		System.out.println(c);
		
		String d="100";
		System.out.println(d);

	}

}
